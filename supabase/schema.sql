create policy "CareLotto rounds can be created"
on public.carelotto_lottery_rounds
for insert
to anon, authenticated
with check (true);

drop policy if exists "CareLotto rounds can be updated" on public.carelotto_lottery_rounds;
create policy "CareLotto rounds can be updated"
on public.carelotto_lottery_rounds
